<h1>Quem é o Mercado Livre?</h1>

<p>Somos uma empresa de tecnologia que tem como objetivo democratizar o comércio eletrônico oferecendo a melhor plataforma e os serviços necessários para que pessoas e empresas possam comprar, pagar, vender, enviar, anunciar e gerir seus negócios na Internet.</p>

<p>Nascemos em 1999, na Argentina, e atualmente, operamos em 18 países. Nosso marketplace – MercadoLivre.com – é o maior da América Latina, reunindo milhões de vendedores e compradores a partir de mais de 60 milhões de ofertas de produtos, automóveis e serviços em tempo real.</p>

<p>O site registra também seis mil buscas e nove vendas por segundo, sendo que 90% dos produtos vendidos são novos. Esses números impactam diretamente a economia brasileira, onde mais de 111 mil pessoas vivem de renda única ou principal, proveniente do Mercado Livre. No país temos 15.480 vendedores com faturamento que permite sustentar 98.648 postos de trabalho. Esses números representam uma fatia de 0,12% do Produto Interno Bruto (PIB) brasileiro.</p>
<h2> História do Mercado Livre: como tudo começou </h2> 
<p>Nossa história começa na Universidade de Stanford, em 1999, quando Marcos Galperin, aluno do MBA da Universidade, se uniu a uma equipe de empreendedores, entre eles Stelleo Tolda, em busca do sonho de revolucionar o e-commerce na América Latina.</p>

<p>Com mais paixão do que recursos, o grupo trabalhou para colocar em marcha o plano do empresário: criar um site de comércio eletrônico que permitiria a qualquer pessoa anunciar e comprar produtos.</p>
